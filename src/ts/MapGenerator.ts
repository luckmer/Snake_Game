class MapGenerator {
  public grid: number[];
  public game: HTMLDivElement[];

  constructor(grid: number[]) {
    this.grid = grid;
    this.game = [];
  }

  createBoard() {
    const grid = this.grid;
    const array = this.game;

    for (let i = 0; i < grid.length; i++) {
      const div = document.createElement("div");
      div.id = grid[i].toString();
      Game?.appendChild(div);
      array.push(div);
      if (grid[i] === 1) {
        array[i].className = "wall";
      }
      if (grid[i] === 0) {
        array[i].className = "space";
      }
    }
  }

  getBoard() {
    const array = this.game;
    return array;
  }
}

const mapGenerator = new MapGenerator(grid);
mapGenerator.createBoard();

export default mapGenerator;
